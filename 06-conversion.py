texto = str(resultado) # Defino a "resultado" como "numero" y al agregarle el "str" lo convierto a Texto
decimal = float(resultado) # Defino a "resultado" como "numero" y al agregarle el "int" lo convierto a Decimal
alternativa = bool(resultado) # Defino a "resultado" como "numero" y al agregarle el "bool" lo convierto a True (1) o False (0)
print(numero + 2)
print(texto + "2")
